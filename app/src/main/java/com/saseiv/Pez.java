package com.saseiv;

public class Pez {
    private int id;
    private String title;
    private String description;
    private String file_url;
    private String audio_url;

    public int getId() {
        return id;
    }

    public String getNombre() {
        return title;
    }

    public String getDescripcion() {
        return description;
    }

    public String getImagen_url() {
        return file_url;
    }

    public String getAudio_url() {
        return audio_url;
    }
}
